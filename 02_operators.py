### Operadores comparativos ###

print (3 > 4)
print (3  < 4)
print (3 >= 4)
print (3 <= 4)
print (3 == 4)
print (3 != 4)

print('Hola' > 'HolaA')
print('Hola' < 'HolaA')
print('Hola' >= 'HolaA') # Ordenacion alfabeetica por ASCII
print(len('Hola') >= len('HolaA')) # Cuenta caracteres
print('Hola' <= 'HolaA')
print('Hola' == 'HolaA')
print('Hola' != 'HolaA')


### Operadores Comparativos ###

print(3 > 4 and 'Hola' > 'HolaA')
print(3 > 4 or 'Hola' > 'HolaA')
print(3 < 4 and 'Hola' < 'HolaA')
print(3 < 4 or 'Hola' < 'HolaA')
print(not(3 > 4))


